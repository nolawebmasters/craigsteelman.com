<h2>&nbsp;</h2>
<h2>SEO Rankings</h2>
<p>&nbsp;</p>
<p>For those of you that don&rsquo;t know, SEO stands for Search Engine Optimization. More simply put, the science and art of getting your website to the top of Google and other search engines. There are all sorts of techies, marketing, and other types that think this is just a bunch of snake-oil salesmanship, and not a &ldquo;real&rdquo; industry or craft. The SEO word has changed a lot of the years. In the early days the naysayers could not have been more wrong.  The system was a game that could be rigged in ones favor  to push a website to the top of Google with relevant keywords that would drive visitors and sales. It&rsquo;s a known fact that the first few listings in Google take the lion&rsquo;s share of the traffic. Being at the top of the rankings is very important for a website.  These top rankings didn&rsquo;t just come by chance, the computer program that created the ranking could be &ldquo;gamed&rdquo;, and the knowledge to play this game was valuable information and one could charge for it. It&rsquo;s also a fair amount of work.</p>
<p>&nbsp;</p>
<p><strong>So how is the SEO game played?</strong></p>
<p>&nbsp;</p>
<p>In the very early days of the internet website, listings and search engines simply reviewed the content and keyword lists of a webpage to build a set of ranking. This system was quickly overrun by those that wanted pages associated with key words with value.  What keywords have value? Well for my story the top of the list are &ldquo;web design&rdquo; and &ldquo;hotel&rdquo;. The web design one is obvious so I&rsquo;m going to leave it out of this story. &ldquo;Hotels&rdquo; were  one of the early  industries to adopt the internet for e commerce. Sites like Hotels.com made it where any joe -six-pack could book their hotel room over the internet instead of calling a travel agent. The hotels.com of the word also had affiliate programs, where they enabled third parties like me to build website with our own brand, use their booking engine software, and if  a sale was made they gave us 5% of the total sale. The trick is to get people to our website. So key words like &ldquo;Discount Hotel rooms&rdquo;, became like gold valuable for us. Now if every joker that figures this out starts putting these keywords all over the pages, the search results themselves get bad in a hurry. Google figured out you could rank websites not just based on their content, but their &ldquo;Internet Popularity&rdquo;, and a good way to  gauge Internet Popularity was to see how many other website link to your website. The term in the industry in known as back linking. Backing was the driving force for SEO for many years.</p>
<p>&nbsp;</p>
<p><strong>Content is King</strong></p>
<p>&nbsp;</p>
<p>Every good SEO guy know content is king, and the back-linking is what grabs everybody&rsquo;s attention. As we all figured out ways of programmatically generating our own backlinks, this system also go overrun by jerks like me that built millions of pages and links build by computer programs and large databases (like hotels.com listing of all the hotels around the word). You still have to have good content that is relevant to your search terms to rank well. Some of the other key factors are clean HTML code, updating your site frequently, and having links with your keywords in them. </p>
