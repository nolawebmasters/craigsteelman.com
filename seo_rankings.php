<p>Euro state key word combos, They mainly use 3 letter state abbreviations, Because of a programming glitch, we could not use the state abbreviation of &ldquo;OR&rdquo; for Oregon, or IN for Indiana, so we renamed the ORE, and IND. We started to notice we got a lot of booking ins Oregon and Indiana. We were one of the only ones that knew this trick, so we were ranked #1 one in Google for things like &ldquo;Hotels, in Salem, ORE&rdquo;, and we could tell from the email address and IP address it was mainly European traffic, Weeks later just about every state had a three letter name to it. It was like finding a pot of gold to hit that.</p>
