<p><strong>The $!#@ Penguin</strong></p>
<p>&nbsp;</p>
<p>15 years after the height of the dotcom boom, the secrets are all officially played out. With the rise of social media Google radically changed their ranking to no longer take &ldquo;backlinking&rdquo; so seriously. Your popularity on social media is now the new in thing.  This new release was officially named &ldquo;Penguin&rdquo; and SEO in now more about social media management and Google&rsquo;s pay to play system Adwords.</p>
