<h2>Austin Pedi Cab Rides</h2>
<p><strong>What is a pedi cab?</strong></p>
<p>&nbsp;</p>
<p>A pedi cab is a bicycle taxi cab. They are big in Austin.  People take them from one end of the bar strip to the other, and back down the  hill to the Hotels in downtown Austin. There are 3 main sections of 6th street.  &quot;Dirty 6th&quot;, is what is traditional know as the bar strip on 6th  street. Many of the older crowd that I take on rides think this place has  changed dramatically over the years, maybe they are just not &quot;young&quot;  anymore. It's probably a little rougher and younger crowd that it was back in  the day. &quot;West 6th&quot; is a somewhat new set of bars about 10 block from  East 6th, that are more Dallas like yuppie 30 something bars. Yes, it's were I  hand out if I go downtown these days. Finally, East 6th has a few dive bars on  the other side of the highway. This is quickly becoming a favorite hot spot for  the hipsters and locals. It used to be a sketchy neighborhood. </p>
<p>&nbsp;</p>
<p><strong>Pedi cab to make you younger</strong><br />
</p>
<p>&nbsp;</p>
<p>So how hard is it to peddle the bike, the trailer, and the  people? Obviously it depends immensely on how much you put into it. My first  ride was pretty rough, it was a long one with three people. At the end of it I  almost threw up and I had sit down for about twenty minutes. The rest of the  night didn't go much better.  I was in  pretty good shape for a 37 year old, but I had not been riding bikes that much  the past few years.  For the first month  or so I would only take rides downhill; something you can actually get away  with in Austin.  Many of the riders sit  in the &quot;Q&quot; or the pedi cab line on 6th street and just wait for  rides. The stronger more motivated riders ride around trying to pick up radon  rides.  It took me about 2 months  before  could ride around for most of a  shift. The typical shift goes from about 10:00 til 3:30 a.m. Now I feel 10  years younger . <br />
</p>
<p>&nbsp;</p>
<p><strong>The power hour of money</strong><br />
</p>
<p>&nbsp;</p>
<p>Typically you make about 1/2 of your money from about 1:30 to  3:00. It's a true mad house when the night comes to and end and a mass of  drunks flow out of the bars. I see crazy things just about every night, and non  I'm not going to put any of them up on this website. It's so much fun I would  almost do it for free.<br />
</p>
<p>&nbsp;</p>
<p><strong>The Super Bowl of Pedi Cabbbing.</strong><br />
</p>
<p>&nbsp;</p>
<p><strong>The largest festival in Austin (and probably Texas), is  South by South West (SXSW)</strong>.  From what  i'm told it's almost Mardi Gras like. Most of the time when you are pedi  cabbing you aren't giving rides. If you were Lance Armstrong and there were  rides galore, you could maybe pull in a G a day. In the immortal words of Bart  Scott &quot;Can't wait&quot;</p>
<h2>&nbsp;</h2>
<h2>SXSW - The Super Bowl of Pedi Cabbing</h2>
<h2>Day 1 of SXSW - IT RAINED AND IT POURED, AND THEN.....</h2>
<p>About a year ago a good buddies of mine talked me into  trying<strong> &quot;Pedi Cabbing&quot;</strong>. For those of you that don't know what this is,  it's the <strong>bicycle taxi</strong>. Austin has one of the biggest pedi cabbing industries in  the country. In recent years it has boomed and spread across the country. These  days the streets of ATX are filled with peddi cabbers.<br />
</p>
<p>&nbsp;</p>
<h2>SXSW Interactive</h2>
<p>&nbsp;</p>
<p>I made it out every day of Interactive. Saturday night it was still pouring rain, but by this time everybody had a canopy of some sorts and we just had to tough it out.The fhird day of Interactive was really nice. Sunny skyies and mild weather made it a super spring day. Also not great for business because by this time everybody was hot to trot on ther own two feet.</p>
<p>&nbsp;</p>
<h2>SXSW Music</h2>
<p>&nbsp;</p>
<p>I only got a few days of Pedi Cabbing in for Music, Like  many newbies to the game, I was a little burnt out near the end of the 10 day  festival.  While I did make big money  during the week, it didn't do any wonders for my IT work, and I didn't feel to  great for about 2 weeks after the biking marathon. </p>
